package hr.fer.opp.projekt.model;

public class Objekt {
	
	private Integer idObjekta;
	private String nazObjekta;
	private Integer brKatova;
	
	public Integer getIdObjekta() {
		return idObjekta;
	}
	public void setIdObjekta(Integer idObjekta) {
		this.idObjekta = idObjekta;
	}
	public String getNazObjekta() {
		return nazObjekta;
	}
	public void setNazObjekta(String nazObjekta) {
		this.nazObjekta = nazObjekta;
	}
	public Integer getBrKatova() {
		return brKatova;
	}
	public void setBrKatova(Integer brKatova) {
		this.brKatova = brKatova;
	}

}
