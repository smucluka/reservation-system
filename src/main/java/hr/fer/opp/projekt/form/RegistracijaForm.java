package hr.fer.opp.projekt.form;

public class RegistracijaForm {
	
	private String ime;
	private String prezime;
	private String email;
	private String ulicaKbr;
	private String grad;
	private String drzava;
	private String brTelefona;
	private String password;
	private String ponovljeniPassword;
	
	public String getIme() {
		return ime;
	}
	public void setIme(String ime) {
		this.ime = ime;
	}
	public String getPrezime() {
		return prezime;
	}
	public void setPrezime(String prezime) {
		this.prezime = prezime;
	}
	public String getEmail() {
		return email;
	}
	public void setEail(String email) {
		this.email = email;
	}
	public String getUlicaKbr() {
		return ulicaKbr;
	}
	public void setUlicaKbr(String ulicaKbr) {
		this.ulicaKbr = ulicaKbr;
	}
	public String getGrad() {
		return grad;
	}
	public void setGrad(String grad) {
		this.grad = grad;
	}
	public String getDrzava() {
		return drzava;
	}
	public void setDrzava(String drzava) {
		this.drzava = drzava;
	}
	public String getBrTelefona() {
		return brTelefona;
	}
	public void setBrTelefona(String brTelefona) {
		this.brTelefona = brTelefona;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPonovljeniPassword() {
		return ponovljeniPassword;
	}
	public void setPonovljeniPassword(String ponovljeniPassword) {
		this.ponovljeniPassword = ponovljeniPassword;
	}

}
